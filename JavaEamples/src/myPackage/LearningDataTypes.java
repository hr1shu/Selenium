package myPackage;

public class LearningDataTypes {

	public static void main(String[] args) {
		
		int    a =25;
		long   b=2424;
		double c=25.4;
		
		System.out.println("the value of int a is "   +a);
		System.out.println("the value of long b is "  +b);
		System.out.println("the value of double c is" +c);
		
		//character 
		
		char charname='a';
		System.out.println("the value of charname is " +charname);
		
		//boolean 
		
		boolean answer=true;
		boolean answer2=false;
		
		System.out.println("the value of answer is" +answer);
		System.out.println("the value of answer2 is" +answer2);
		System.out.println(2>3);
		
		//operator 
		a++;
		System.out.println(a);
		--a;
		--a;
		System.out.println(--a);
		
	}

}
